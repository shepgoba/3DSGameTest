int main(int argc, char* argv[])
{
    const u32 clrClear = C2D_Color32(0xFF, 0xFF, 0xFF, 0xFF);

    gfxInitDefault();
    C3D_Init(C3D_DEFAULT_CMDBUF_SIZE);
    C2D_Init(C2D_DEFAULT_MAX_OBJECTS);
    C2D_Prepare();
    consoleInit(GFX_BOTTOM, NULL);

    C3D_RenderTarget* top = C2D_CreateScreenTarget(GFX_TOP, GFX_LEFT);

    Player player = CreatePlayer(20, 20, 10, 20, RED);

    int blockCount = 0;
    Block blocks[WORLD_WIDTH * WORLD_HEIGHT];
    
    for (int y = 0; y < WORLD_HEIGHT; y++)
    {
        for (int x = 0; x < WORLD_WIDTH; x++)
        {
            if (world[y][x] == 1)
            {
                blocks[blockCount] = CreateBlock(x * 10, y * 10, 10, 10, 1);
                blockCount++;
            }
        }
    }

    while (aptMainLoop())
	{
		hidScanInput();

		u32 kDown = hidKeysDown();
        u32 kUp = hidKeysUp();

        handleKeyPresses(&kDown, &kUp, keys);

		C3D_FrameBegin(C3D_FRAME_SYNCDRAW);
		C2D_TargetClear(top, clrClear);
		C2D_SceneBegin(top);

        UpdatePlayer(&player);
        DrawPlayer(&player);

        for (int i = 0; i < blockCount; i++)
        {
            CollideBlockY(&blocks[i], &player);
            //CollideBlockX(&blocks[i], &player);
            DrawBlock(&blocks[i]);
        }
		C3D_FrameEnd(0);

	}

	C2D_Fini();
	C3D_Fini();
	gfxExit();

    return 0;
}
